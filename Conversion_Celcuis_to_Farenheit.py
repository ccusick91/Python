print("\n\t\tHello, This app converts Celcius to Farenheit")

# Equation for conversion is F=C*9/5+32
# f=farenheit
# c=celcius

input1 = int(input("\n\t\tPlease enter degrees Celcius: "))

degreesFarenheit = input1 * 9/5 + 32

print("\n\t\tDegrees farenheit: {} degrees".format(degreesFarenheit))

