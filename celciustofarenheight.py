print("\n\t\tHello, This app converts Celcius to Farenheight")

# Equation for conversion is F=C*9/5+32
# f=farenheight
# c=celcius

input1 = int(input("\n\t\tPlease enter degrees Celcius: "))

degreesFarenheight = input1 * 9/5 + 32

print("\n\t\tDegrees farenheight: {} degrees".format(degreesFarenheight))

